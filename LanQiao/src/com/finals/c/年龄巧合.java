package com.finals.c;

public class 年龄巧合 {
	public static void main(String[] args) {
		for (int i = 1995; i < 2014; i++) {
			int count = i/1000 + i/100%10 + i/10%10 + i%10;
			if(count == 2014 - i){
				System.out.println(i);
			}
			
		}
	}
}
